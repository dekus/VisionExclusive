RegisterCommand('casa',function(source,args,rawCommand)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		if args[1] == "aggiungi" and homes[tostring(args[2])] and parseInt(args[3]) > 0 then
			local myHomes = vRP.query("homes/get_homeuserowner",{ user_id = parseInt(user_id), home = tostring(args[2]) })
			if myHomes[1] then
				local totalResidents = vRP.query("homes/count_homepermissions",{ home = tostring(args[2]) })
				if parseInt(totalResidents[1].qtd) >= parseInt(homes[tostring(args[2])][3]) then
					TriggerClientEvent("Notify",source,"negato","La residenza "..tostring(args[2]).." ha raggiunto il numero massimo di residenti.",10000)
					return
				end

				local identity = vRP.getUserIdentity(parseInt(args[3]))
				local totalHomes = vRP.query("homes/count_homes",{ user_id = parseInt(args[3]) })

				if vRP.hasPermission(parseInt(args[3]),'plus.autorizzazione') then
					if parseInt(totalHomes[1].qtd) >= 4 then
						TriggerClientEvent("Notify",source,"negato","<b>"..identity.name.." "..identity.firstname.."</b> ha raggiunto il numero massimo di residenze.",10000)
						return
					end
				else
					if parseInt(totalHomes[1].qtd) >= 2 then
						TriggerClientEvent("Notify",source,"negato","<b>"..identity.name.." "..identity.firstname.."</b> ha raggiunto il numero massimo di residenze.",10000)
						return
