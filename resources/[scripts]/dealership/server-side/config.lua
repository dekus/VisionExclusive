count = 0
Citizen.CreateThread( function()
    for k,v in pairs(vehicleList) do 
        for l,w in pairs(v) do 
            local estoque = vRP.query("storeEstoque",{vehicle = l })
            if estoque[1] and estoque[1].estoque then 
                if parseInt(estoque[1].estoque) <= 0 then 
                    vehicleList[k][l] = nil 
                end
                count = count + 1
                w.estoque = estoque[1].estoque
            --else
            --    vRP.execute("insertEstoque",{ vehicle = l })
            end
        end        
    end
    vehicleList = vehicleList
end)

getEstoque = function(category,car)
    if vehicleList[category] and vehicleList[category][car] then 
        return vehicleList[category][car].estoque
    end
end

reduceEstoque = function(category,car)
    if vehicleList[category] and vehicleList[category][car] then 
        vehicleList[category][car].estoque = vehicleList[category][car].estoque - 1
        vRP.execute("updateStock",{ vehicle = car, estoque =  vehicleList[category][car].estoque})
    end
end

vehInfos = function(category,car)
    if vehicleList[category] and vehicleList[category][car] then 
        return vehicleList[category][car]
    end
end

